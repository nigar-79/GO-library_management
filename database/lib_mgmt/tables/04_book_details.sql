CREATE TABLE LIB_MGMT.BOOK_DEATILS (
	BOOK_ID INTEGER DEFAULT NEXTVAL('LIB_MGMT.SEQ_BOOK_DEATILS_BOOK_ID'),
	ISBN VARCHAR(20) NOT NULL,
	BOOK_TITLE VARCHAR(200) NOT NULL,
	LANGUAGE VARCHAR(50),
	TOTAL_NO_OF_COPIES INTEGER NOT NULL,
	CATEGORY_ID INTEGER NOT NULL,
	BINDING_PUBLICATION_ID INTEGER NOT NULL,
	SHELF_ID INTEGER NOT NULL,
   	CONSTRAINT BOOK_ID_PKEY PRIMARY KEY(BOOK_ID),
	CONSTRAINT ISBN_CODE_UK UNIQUE(ISBN),
	CONSTRAINT BOOK_ID_FKEY FOREIGN KEY(CATEGORY_ID) 
		REFERENCES LIB_MGMT.BOOK_CATEGORY(CATEGORY_ID),
	CONSTRAINT BINDING_PUBLICATION_ID_FKEY FOREIGN KEY (BINDING_PUBLICATION_ID) 
		REFERENCES LIB_MGMT.BOOK_BINDING_PUBLICATION(PUBLICATION_ID),
	CONSTRAINT SHELF_ID_FKEY FOREIGN KEY(SHELF_ID) 
	REFERENCES LIB_MGMT.BOOK_SHELVES(SHELF_ID)
);
